package com.guiseframework.geometry;

/**Indicates an absolute side.
@author Garret Wilson
@see Axis
@see com.guiseframework.component.layout.Border
*/
public enum Side
{

	/**The left side.*/
	LEFT,

	/**The right side.*/
	RIGHT,

	/**The top side.*/
	TOP,
	
	/**The bottom side.*/
	BOTTOM,

	/**The back side.
	This value is currently unused.
	*/
	FRONT,

	/**The front side.
	This value is currently unused.
	*/
	BACK;
}
